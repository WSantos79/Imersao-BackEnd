# Habilita o serviço Cloud Run, que permite executar containers sem servidor
gcloud services enable run.googleapis.com

# Habilita o serviço Cloud Build, que automatiza a criação e implantação de aplicativos
gcloud services enable cloudbuild.googleapis.com

# Habilita o serviço Artifact Registry, que armazena e gerencia pacotes de software
gcloud services enable artifactregistry.googleapis.com